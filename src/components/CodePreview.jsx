export default function CodePreview({ prompt }) {
    const [copied, setCopied] = useState(false);
    const code = `// ${prompt}
    import { useState } from "react";

    export default function Component() {
    const [active, setActive] = useState(false);

    return (
        <div className="min-h-screen bg-[#0a0a0a] text-white
                        flex items-center justify-center">
            <div className="max-w-4xl w-full mx-auto px-6">
                <h1 className="text-5xl font-bold tracking-tight mb-4">
                Built with v0
                </h1>
                <p className="text-[#888] text-lg mb-8">
                Production-ready component generated instantly.
                </p>
                <button
                onClick={() => setActive(!active)}
                className="px-6 py-3 bg-white text-black rounded-xl
                            font-semibold hover:bg-[#e5e5e5] transition-all"
                >
                {active ? "Active!" : "Click me"}
                </button>
            </div>
        </div>
        );
    }`;

    const handleCopy = () => {
        navigator.clipboard.writeText(code).then(() => {
            setCopied(true);
            setTimeout(() => setCopied(false), 2000);
        });
    };

    return (
        <div className="mt-4 rounded-xl border border-[#2a2a2a] overflow-hidden bg-[#0d0d0d]">
            <div className="flex items-center justify-between px-4 py-2.5 border-b border-[#1f1f1f]">
                <div className="flex items-center gap-2 text-[12px] text-[#666]">
                    {icons.CodeIcon} 
                    <span>Component.jsx</span>
                </div>
                <button
                    onClick={handleCopy}
                    className="flex items-center gap-1.5 px-2.5 py-1 rounded-md bg-[#1a1a1a] border border-[#2a2a2a] text-[#888] text-[11px] hover:text-[#ccc] hover:border-[#3a3a3a] transition-all"
                >
                    {icons.CopyIcon} 
                    {copied ? "Copied!" : "Copy"}
                </button>
            </div>
            <pre className="px-4 py-4 text-[12px] text-[#a8a8a8] overflow-x-auto leading-relaxed font-mono">
                <code>{code}</code>
            </pre>
        </div>
    );
}
